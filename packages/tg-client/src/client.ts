import { TelegramClient } from "@mtcute/bun";
import { Long, MemoryStorage } from "@mtcute/core";
import { env } from "@selectio/config";

const API_ID = Number.parseInt(env.TELEGRAM_API_ID || "0", 10);
const API_HASH = env.TELEGRAM_API_HASH || "";
const BOT_TOKEN = env.TELEGRAM_BOT_TOKEN || "";
if (!API_ID || !API_HASH || !BOT_TOKEN) {
  throw new Error(
    "TELEGRAM_API_ID, TELEGRAM_API_HASH –∏ TELEGRAM_BOT_TOKEN –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã",
  );
}

// –°–æ–∑–¥–∞–µ–º –∫–ª–∏–µ–Ω—Ç –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π
export const tg = new TelegramClient({
  apiId: API_ID,
  apiHash: API_HASH,
  storage: new MemoryStorage(),
});

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –∫–ª–∏–µ–Ω—Ç–∞
let isInitialized = false;

export async function initClient() {
  if (isInitialized) return;

  try {
    await tg.start({
      botToken: BOT_TOKEN,
    });
    isInitialized = true;
    console.log("‚úÖ MTCute –∫–ª–∏–µ–Ω—Ç –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω");
  } catch (error) {
    console.error("‚ùå –û—à–∏–±–∫–∞ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ MTCute –∫–ª–∏–µ–Ω—Ç–∞:", error);
    throw error;
  }
}

/**
 * –û—Ç–ø—Ä–∞–≤–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –ø–æ username
 * @param username - Username –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (—Å @ –∏–ª–∏ –±–µ–∑)
 * @param text - –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è
 */
export async function sendMessageByUsername(
  username: string,
  text: string,
): Promise<{ success: boolean; message: string; chatId?: string }> {
  try {
    await initClient();

    // –£–±–∏—Ä–∞–µ–º @ –µ—Å–ª–∏ –µ—Å—Ç—å
    const cleanUsername = username.startsWith("@")
      ? username.slice(1)
      : username;
    console.log("cleanUsername", cleanUsername);
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    const result = await tg.sendText("BotCraftEngineer", text);

    return {
      success: true,
      message: "–°–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ",
      chatId: result.chat.id.toString(),
    };
  } catch (error) {
    console.error("–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è:", error);
    return {
      success: false,
      message: error instanceof Error ? error.message : "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞",
    };
  }
}

/**
 * –û—Ç–ø—Ä–∞–≤–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –ø–æ –Ω–æ–º–µ—Ä—É —Ç–µ–ª–µ—Ñ–æ–Ω–∞
 * –î–æ–±–∞–≤–ª—è–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç –≤ Telegram –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ
 * @param phone - –ù–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –≤ –º–µ–∂–¥—É–Ω–∞—Ä–æ–¥–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ (–Ω–∞–ø—Ä–∏–º–µ—Ä, +79991234567)
 * @param text - –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è
 * @param firstName - –ò–º—è –∫–æ–Ω—Ç–∞–∫—Ç–∞ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
 */
export async function sendMessageByPhone(
  phone: string,
  text: string,
  firstName?: string,
): Promise<{ success: boolean; message: string; chatId?: string }> {
  try {
    await initClient();

    // –û—á–∏—â–∞–µ–º –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –æ—Ç –ª–∏—à–Ω–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤
    const cleanPhone = phone.replace(/[^\d+]/g, "");

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–æ—Ä–º–∞—Ç –Ω–æ–º–µ—Ä–∞
    if (!cleanPhone.startsWith("+")) {
      return {
        success: false,
        message:
          "–ù–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –≤ –º–µ–∂–¥—É–Ω–∞—Ä–æ–¥–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ (–Ω–∞—á–∏–Ω–∞—Ç—å—Å—è —Å +)",
      };
    }

    console.log(`üìû –ü–æ–ø—ã—Ç–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ –Ω–æ–º–µ—Ä—É: ${cleanPhone}`);

    // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –∫–æ–Ω—Ç–∞–∫—Ç –≤ Telegram
    const importResult = await tg.call({
      _: "contacts.importContacts",
      contacts: [
        {
          _: "inputPhoneContact",
          clientId: Long.fromNumber(Date.now()),
          phone: cleanPhone,
          firstName: firstName || "–ö–∞–Ω–¥–∏–¥–∞—Ç",
          lastName: "",
        },
      ],
    });

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏–º–ø–æ—Ä—Ç–∞
    if (!importResult.users || importResult.users.length === 0) {
      console.log(
        `‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å –Ω–æ–º–µ—Ä–æ–º ${cleanPhone} –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ Telegram`,
      );
      return {
        success: false,
        message: "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º –Ω–æ–º–µ—Ä–æ–º —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ Telegram",
      };
    }

    const user = importResult.users[0];
    if (!user || user._ !== "user") {
      return {
        success: false,
        message: "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è",
      };
    }

    console.log(`‚úÖ –ö–æ–Ω—Ç–∞–∫—Ç –∏–º–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω: ${user.id}`);

    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    const result = await tg.sendText(user.id, text);

    return {
      success: true,
      message: "–°–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ",
      chatId: result.chat.id.toString(),
    };
  } catch (error) {
    console.error("‚ùå –û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ —Ç–µ–ª–µ—Ñ–æ–Ω—É:", error);
    return {
      success: false,
      message: error instanceof Error ? error.message : "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞",
    };
  }
}

/**
 * –ü—Ä–æ–≤–µ—Ä–∏—Ç—å —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–æ username
 */
export async function checkUsername(
  username: string,
): Promise<{ exists: boolean; chatId?: string }> {
  try {
    await initClient();

    const cleanUsername = username.startsWith("@")
      ? username.slice(1)
      : username;

    const peer = await tg.resolvePeer(cleanUsername);

    return {
      exists: true,
      chatId: String(peer),
    };
  } catch {
    return {
      exists: false,
    };
  }
}
